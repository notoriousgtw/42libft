#include "libft.h"
#include "ft_bit_utils.h"

uint64_t	ft_getbitstr(uint64_t x, uint8_t b, uint8_t n)
{
	uint64_t y;
	y = (x >> b);
	y = (y << (64 - n)) >> (64 - n);

	return (y);
}
